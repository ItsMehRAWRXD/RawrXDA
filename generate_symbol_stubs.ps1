$Win32IDEStubFile = Join-Path $OutDir "win32ide_stubs.asm"
$Win32IDEStubs | Out-File -FilePath $Win32IDEStubFile -Encoding UTF8

# Generate GPU accelerator stubs
$GPUStubs = @"
; GPU Accelerator Stub Implementations
; Generated by generate_symbol_stubs.ps1

.code

; GPUAccelerator stub class methods
GPUAccelerator_Initialize PROC
    ; Return success
    mov rax, 1
    ret
GPUAccelerator_Initialize ENDP

GPUAccelerator_Execute PROC
    ; Return success
    mov rax, 1
    ret
GPUAccelerator_Execute ENDP

GPUAccelerator_Cleanup PROC
    ; Stub implementation
    ret
GPUAccelerator_Cleanup ENDP

; GPUKernel stub methods
GPUKernel_Load PROC
    ; Return success
    mov rax, 1
    ret
GPUKernel_Load ENDP

GPUKernel_Run PROC
    ; Return success
    mov rax, 1
    ret
GPUKernel_Run ENDP

; GPUBuffer stub methods
GPUBuffer_Allocate PROC
    ; Return mock pointer
    mov rax, 12345678h
