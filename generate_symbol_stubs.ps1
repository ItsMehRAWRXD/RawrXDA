    ; Stub implementation
    ret
GPUBuffer_Free ENDP

END
"@

$GPUStubFile = Join-Path $OutDir "gpu_stubs.asm"
$GPUStubs | Out-File -FilePath $GPUStubFile -Encoding UTF8

# Generate printf family stubs (minimal implementations)
$PrintfStubs = @"
; Printf Family Stub Implementations  
; Generated by generate_symbol_stubs.ps1

includelib kernel32.lib
includelib user32.lib

.code

; printf stub - writes to debug output
printf PROC
    ; Simple stub - just return character count
    mov rax, 0
    ret
printf ENDP

; sprintf stub 
sprintf PROC  
    ; Simple stub - just return 0
    xor rax, rax
    ret
sprintf ENDP

; fprintf stub
fprintf PROC
    ; Simple stub - just return 0
    xor rax, rax
    ret  
fprintf ENDP

END
"@

$PrintfStubFile = Join-Path $OutDir "printf_stubs.asm"
$PrintfStubs | Out-File -FilePath $PrintfStubFile -Encoding UTF8

# Compile all stub files
$StubFiles = @($Win32IDEStubFile, $GPUStubFile, $PrintfStubFile)
$CompiledStubs = @()

foreach($stubFile in $StubFiles) {
    $objFile = [IO.Path]::ChangeExtension($stubFile, ".obj")
    $objName = [IO.Path]::GetFileName($objFile)
    
    Write-Host "[COMPILING] $objName..." -Fore Yellow
    
    try {
        $result = Start-Process -FilePath $Masm -ArgumentList "/c", "/Fo`"$objFile`"", "`"$stubFile`"" -Wait -PassThru -NoNewWindow
        
        if($result.ExitCode -eq 0 -and (Test-Path $objFile)) {
            Write-Host "  [SUCCESS] $objName compiled" -Fore Green
            $CompiledStubs += $objFile
        } else {
            Write-Host "  [FAILED] $objName compilation failed" -Fore Red
        }
    } catch {
        Write-Host "  [ERROR] Failed to compile $objName`: $($_.Exception.Message)" -Fore Red
    }
}

# Create stub library
if($CompiledStubs.Count -gt 0) {
    # Find lib.exe
    $LibPaths = @(
        "C:\Program Files (x86)\Microsoft Visual Studio\2022\BuildTools\VC\Tools\MSVC\14.44.35207\bin\Hostx64\x64\lib.exe",
        "C:\Program Files\Microsoft Visual Studio\2022\Enterprise\VC\Tools\MSVC\14.44.35207\bin\Hostx64\x64\lib.exe",
        "C:\Program Files\Microsoft Visual Studio\2022\Professional\VC\Tools\MSVC\14.44.35207\bin\Hostx64\x64\lib.exe"
    )
    
    $Lib = $LibPaths | Where-Object { Test-Path $_ } | Select-Object -First 1
    
    if($Lib) {
        $StubLibrary = Join-Path $OutDir "rawrxd_stubs.lib"
        Write-Host "[CREATING] Stub library: $StubLibrary..." -Fore Yellow
        
        $libArgs = @("/OUT:`"$StubLibrary`"") + ($CompiledStubs | ForEach-Object { "`"$_`"" })
        
        try {
            $libResult = Start-Process -FilePath $Lib -ArgumentList $libArgs -Wait -PassThru -NoNewWindow
            
            if($libResult.ExitCode -eq 0 -and (Test-Path $StubLibrary)) {
                Write-Host "  [SUCCESS] Stub library created" -Fore Green
                Write-Host "  [INFO] Add the following to genesis_build.ps1:" -Fore Cyan
                Write-Host "    `$linkArgs += `"`"$StubLibrary`"`"" -Fore White
            }
        } catch {
            Write-Host "  [WARNING] Could not create stub library: $($_.Exception.Message)" -Fore Yellow
        }
    }
}

Write-Host "`n============================================================" -Fore Green
Write-Host " SYMBOL STUB GENERATION COMPLETE" -Fore Green
Write-Host " Generated Stubs: $($CompiledStubs.Count)" -Fore White
Write-Host " Stub Directory: $OutDir" -Fore Gray
Write-Host " TRACK 2: Missing symbol stubs created" -Fore Green
Write-Host "============================================================" -Fore Green

# Output stub information for integration
@{
    StubDirectory = $OutDir
    CompiledStubs = $CompiledStubs
    StubLibrary = if($Lib) { Join-Path $OutDir "rawrxd_stubs.lib" } else { $null }
    GeneratedFiles = $StubFiles + $CompiledStubs
} | ConvertTo-Json | Out-File -FilePath (Join-Path $OutDir "stub_manifest.json") -Encoding UTF8