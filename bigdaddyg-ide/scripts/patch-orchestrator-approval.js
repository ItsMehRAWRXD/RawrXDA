const fs = require('fs');
const path = require('path');

const PREFIX = '[patch-orchestrator-approval]';

const p = path.join(__dirname, '..', 'src', 'agentic', 'orchestrator.js');
const s = fs.readFileSync(p, 'utf8');

const start = s.indexOf('  buildApprovalSummary(step) {');
const end = s.indexOf('\n  async runReflection', start);
if (start < 0 || end < 0) {
  console.error(`${PREFIX} Anchors not found in orchestrator.js (buildApprovalSummary(step) before async runReflection).`, {
    start,
    end
  });
  console.error(`${PREFIX} Next: open src/agentic/orchestrator.js and restore that method order, or skip this one-off patch if the file was refactored.`);
  process.exit(1);
}

const newBlock = `  buildApprovalSummary(step) {
    const lines = [\`Action: \${step.type}\`, \`Path: \${step.path || '(n/a)'}\`];
    const st = (step.type || '').toLowerCase();
    if (st === 'ask_ai') {
      const pr = step.prompt || step.description || '';
      lines.push(\`Prompt preview (\${pr.length} chars):\`, pr.slice(0, 1200) + (pr.length > 1200 ? '…' : ''));
      return lines.join('\n');
    }
    if (step.content != null) {
      const c = String(step.content);
      lines.push(\`Content preview (\${c.length} chars):\`, c.slice(0, 800) + (c.length > 800 ? '…' : ''));
    } else if (st === 'write_file' || st === 'append_file') {
      lines.push('Content will be generated by the model if not provided in the plan.');
    }
    return lines.join('\n');
  }`;

const out = s.slice(0, start) + newBlock + s.slice(end);
fs.writeFileSync(p, out);
console.log(`${PREFIX} Patched`, p);
