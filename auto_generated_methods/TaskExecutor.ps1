#Requires -Version 7.4

<#
.SYNOPSIS
    Autonomous Task Execution and Progress Tracking Module.
.DESCRIPTION
    Executes tasks generated by the DirectoryScanner module and tracks progress.
#>

function Execute-Tasks {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [array]$Tasks,

        [Parameter(Mandatory=$false)]
        [string]$ProgressFile = "$env:TEMP\TaskProgress.json"
    )

    $progress = @()

    if (Test-Path $ProgressFile) {
        Write-Host "Loading existing progress from: $ProgressFile" -ForegroundColor Cyan
        $progress = Get-Content -Path $ProgressFile | ConvertFrom-Json
    }

    foreach ($task in $Tasks) {
        $existingTask = $progress | Where-Object { $_.FileName -eq $task.FileName -and $_.Task -eq $task.Task }

        if ($existingTask -and $existingTask.Status -eq 'Completed') {
            Write-Host "Skipping completed task: $($task.FileName) - $($task.Task)" -ForegroundColor Green
            continue
        }

        Write-Host "Executing task: $($task.FileName) - $($task.Task)" -ForegroundColor Yellow

        try {
            # Simulate task execution (replace with real logic)
            Start-Sleep -Seconds (Get-Random -Minimum 1 -Maximum 3)

            $progress += [PSCustomObject]@{
                FileName = $task.FileName
                Task     = $task.Task
                Status   = 'Completed'
                Timestamp = Get-Date -Format 'yyyy-MM-dd HH:mm:ss'
            }

            Write-Host "Task completed: $($task.FileName) - $($task.Task)" -ForegroundColor Green
        } catch {
            $progress += [PSCustomObject]@{
                FileName = $task.FileName
                Task     = $task.Task
                Status   = 'Failed'
                Error    = $_.Exception.Message
                Timestamp = Get-Date -Format 'yyyy-MM-dd HH:mm:ss'
            }

            Write-Host "Task failed: $($task.FileName) - $($task.Task)" -ForegroundColor Red
        }

        # Save progress after each task
        $progress | ConvertTo-Json -Depth 10 | Set-Content -Path $ProgressFile -Encoding UTF8
    }

    Write-Host "All tasks executed. Progress saved to: $ProgressFile" -ForegroundColor Cyan
    return $progress
}

# Example usage
if ($MyInvocation.InvocationName -ne '.') {
    $taskFile = Read-Host "Enter the path to the task file (JSON)"

    if (-Not (Test-Path $taskFile)) {
        Write-Host "Task file not found: $taskFile" -ForegroundColor Red
        exit 1
    }

    $tasks = Get-Content -Path $taskFile | ConvertFrom-Json
    Execute-Tasks -Tasks $tasks
}