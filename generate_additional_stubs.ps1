Write-Host "[INFO] Creating additional stub files..." -Fore White

# Write stub files
$WindowsAPIStubFile = Join-Path $OutDir "winapi_additional_stubs.asm"
$AccelRouterStubFile = Join-Path $OutDir "accelrouter_stubs.asm"
$VulkanKernelStubFile = Join-Path $OutDir "vulkankernel_stubs.asm"

$WindowsAPIStubs | Out-File -FilePath $WindowsAPIStubFile -Encoding UTF8
$AccelRouterStubs | Out-File -FilePath $AccelRouterStubFile -Encoding UTF8
$VulkanKernelStubs | Out-File -FilePath $VulkanKernelStubFile -Encoding UTF8

# Compile the additional stubs
$AdditionalStubFiles = @($WindowsAPIStubFile, $AccelRouterStubFile, $VulkanKernelStubFile)
$NewCompiledStubs = @()

foreach($stubFile in $AdditionalStubFiles) {
    $objFile = [IO.Path]::ChangeExtension($stubFile, ".obj")  
    $objName = [IO.Path]::GetFileName($objFile)
    
    Write-Host "[COMPILING] $objName..." -Fore Yellow
    
    try {
        $result = Start-Process -FilePath $Masm -ArgumentList "/c", "/Fo`"$objFile`"", "`"$stubFile`"" -Wait -PassThru -NoNewWindow
        
        if($result.ExitCode -eq 0 -and (Test-Path $objFile)) {
            Write-Host "  [SUCCESS] $objName compiled" -Fore Green
            $NewCompiledStubs += $objFile
        } else {
            Write-Host "  [FAILED] $objName compilation failed" -Fore Red
        }
    } catch {
        Write-Host "  [ERROR] Failed to compile $objName`: $($_.Exception.Message)" -Fore Red
    }
}

Write-Host "`n============================================================" -Fore Green
Write-Host " ADDITIONAL STUB GENERATION COMPLETE" -Fore Green
Write-Host " Additional Stubs: $($NewCompiledStubs.Count)" -Fore White
Write-Host " Files: $($NewCompiledStubs -join '; ')" -Fore Gray
Write-Host "============================================================" -Fore Green