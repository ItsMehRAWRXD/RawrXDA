PrintString PROC
    push rbx
    push r12
    push r13
    sub rsp, 40h
    
    mov r12, rcx
    xor r13, r13
strlen_loop:
    cmp byte ptr [r12 + r13], 0
    je strlen_done
    inc r13
    jmp strlen_loop
    
strlen_done:
    mov ecx, STD_OUTPUT_HANDLE
    call GetStdHandle
    mov rbx, rax
    
    mov rcx, rbx
    mov rdx, r12
    mov r8, r13
    lea r9, [rsp+30h]
    mov qword ptr [rsp+20h], 0
    call WriteFile
    
    add rsp, 40h
    pop r13
    pop r12
    pop rbx
    ret
PrintString ENDP

