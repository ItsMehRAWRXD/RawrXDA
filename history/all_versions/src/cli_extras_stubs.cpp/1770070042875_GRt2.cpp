bool APIServer::Start(uint16_t port) {
    if (is_running_) return false;
    
    port_ = port;
    is_running_ = true;
    
    server_thread_ = std::make_unique<std::thread>([this]() {
        WSADATA wsaData;
        WSAStartup(MAKEWORD(2, 2), &wsaData);
        
        SOCKET listenSocket = socket(AF_INET, SOCK_STREAM, IPPROTO_TCP);
        if (listenSocket == INVALID_SOCKET) return;
        
        sockaddr_in serverAddr;
        serverAddr.sin_family = AF_INET;
        serverAddr.sin_addr.s_addr = INADDR_ANY;
        serverAddr.sin_port = htons(port_);
        
        if (bind(listenSocket, (sockaddr*)&serverAddr, sizeof(serverAddr)) == SOCKET_ERROR) {
            closesocket(listenSocket);
            return;
        }
        
        if (listen(listenSocket, SOMAXCONN) == SOCKET_ERROR) {
            closesocket(listenSocket);
            return;
        }
        
        std::cout << "[APIServer] Listening on port " << port_ << std::endl;

        while (is_running_) {
            fd_set readfds;
            FD_ZERO(&readfds);
            FD_SET(listenSocket, &readfds);
            
            timeval timeout;
            timeout.tv_sec = 1;
            timeout.tv_usec = 0;
            
            int activity = select(0, &readfds, NULL, NULL, &timeout);
            
            if (activity > 0 && FD_ISSET(listenSocket, &readfds)) {
                SOCKET clientSocket = accept(listenSocket, NULL, NULL);
                if (clientSocket != INVALID_SOCKET) {
                    std::thread([this, clientSocket]() {
                        std::string rawReq = ReadSocket(clientSocket);
                        if (rawReq.empty()) { closesocket(clientSocket); return; }
                        
                        // Simple Routing
                        std::string url;
                        std::string method;
                        std::istringstream stream(rawReq);
                        stream >> method >> url;
                        
                        std::string responseBody;
                        std::string contentType = "application/json";
                        
                        try {
                            if (url == "/api/generate" && method == "POST") {
                                size_t jsonStart = rawReq.find("{");
                                if (jsonStart != std::string::npos) {
                                    std::string jsonStr = rawReq.substr(jsonStart);
                                    HandleGenerateRequest(jsonStr, responseBody);
                                }
                            } else if ((url == "/api/chat" || url == "/v1/chat/completions") && method == "POST") {
                                size_t jsonStart = rawReq.find("{");
                                if (jsonStart != std::string::npos) {
                                    std::string jsonStr = rawReq.substr(jsonStart);
                                    HandleChatCompletion(jsonStr, responseBody);
                                }
                            } else {
                                responseBody = "{\"error\":\"Not Found\"}";
                            }
                        } catch (...) {
                            responseBody = "{\"error\":\"Internal Server Error\"}";
                        }
                        
                        SendResponse(clientSocket, "200 OK", contentType, responseBody);
                        closesocket(clientSocket);
                    }).detach();
                }
            }
        }
        
        closesocket(listenSocket);
        WSACleanup();
    });
    
    return true;
}

bool APIServer::Stop() {
    if (!is_running_) return false;
    is_running_ = false;
    if (server_thread_ && server_thread_->joinable()) {
        server_thread_->join();
        server_thread_.reset();
    }
    return true;
}

