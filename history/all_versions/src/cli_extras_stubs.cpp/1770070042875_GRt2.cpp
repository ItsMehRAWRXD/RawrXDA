void OverclockGovernor::RunLoop(AppState* state) {
    while (running_) {
        TelemetrySnapshot snap;
        if (telemetry::Poll(snap)) {
            // snap.cpuTempC = 50.0;
            // snap.cpuTempValid = true;
            float target_temp = 90.0f; 
            if (snap.cpuTempValid && snap.cpuTempC > target_temp) {
                 overclock_vendor::ApplyCpuOffsetMhz(-25);
            }
        }
        std::this_thread::sleep_for(std::chrono::milliseconds(500));
    }
}

