void APIServer::HandleGenerateRequest(const std::string& requestInfo, std::string& response) {
    try {
        auto j = json::parse(requestInfo);
        std::string prompt = j.value("prompt", "");
        
        std::string text;
        if (app_state_.inference_engine) {
            auto res = app_state_.inference_engine->generate(prompt);
            if (res.has_value()) text = res.value().text;
            else text = "Error: Inference failed.";
        } else {
            // Try static instance as fallback
            auto engine = RawrXD::CPUInferenceEngine::getInstance();
            if (engine) {
                auto res = engine->generate(prompt);
                if (res.has_value()) text = res.value().text;
                else text = "Error: Inference failed.";
            } else {
                 text = "APIServer: No Inference Engine Loaded.";
            }
        }
        
        json resp;
        resp["response"] = text;
        resp["done"] = true;
        response = resp.dump();
    } catch (...) {
        response = "{\"error\":\"Bad Request\"}";
    }
}

