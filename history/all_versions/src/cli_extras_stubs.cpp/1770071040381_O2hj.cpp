class ReactServerGenerator {
public:
    static bool Generate(const std::string& projectDir, const std::string& name) {
        try {
            std::filesystem::create_directories(projectDir + "/public");
            
            // 1. package.json
            std::ofstream pkg(projectDir + "/package.json");
            pkg << R"JSON({
  "name": "rawrxd-app",
  "version": "1.0.0",
  "description": "Generated by RawrXD",
  "main": "server.js",
  "dependencies": { "express": "^4.18.2" }
})JSON";

            // 2. server.js
            std::ofstream srv(projectDir + "/server.js");
            srv << R"JS(const express = require('express');
const app = express();
const PORT = 3000;
app.use(express.static('public'));
app.get('*', (req, res) => res.send('RawrXD React App'));
app.listen(PORT, () => {
    console.log(`Server running on ${PORT}`);
});
)JS";

            // 3. public/index.html
            std::ofstream idx(projectDir + "/public/index.html");
            idx << R"HTML(<!DOCTYPE html>
<html>
<body>
    <div id="root"><h1>Generated App</h1></div>
</body>
</html>
)HTML";
            return true;
        } catch (...) { return false; }
    }
};

