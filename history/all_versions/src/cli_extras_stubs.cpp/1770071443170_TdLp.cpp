std::string AgenticEngine::planTask(const std::string& task) {
    if (!m_inferenceEngine) return "Error: No Engine";
    
    std::string prompt = "Create a detailed step-by-step plan for: " + task;
    auto res = m_inferenceEngine->generate(prompt, 0.7f, 0.9f, 2048);
    if(res.has_value()) return res.value().text;
    return "Error: Planning failed. Check engine status.";
}

nlohmann::json AgenticEngine::planTask(const std::string& goal, bool returnJson) {
     if (!m_inferenceEngine) return {{"error", "No Engine"}};
     
     std::string schema = R"([{"type":"command|edit", "target":"filename", "description":"..."}])";
     std::string prompt = "Generate a JSON execution plan for: " + goal + "\nStrict JSON Array Schema: " + schema;
     
     auto res = m_inferenceEngine->generate(prompt, 0.2f, 0.9f, 2048); // Low temp for JSON
     if(res.has_value()) {
         try {
             std::string text = res.value().text;
             size_t s = text.find("[");
             size_t e = text.rfind("]");
             if(s != std::string::npos && e != std::string::npos) {
                 return nlohmann::json::parse(text.substr(s, e-s+1));
             }
         } catch(...) {}
         return {{"warning", "Failed to parse JSON"}, {"raw_response", res.value().text}};
     }
     return {{"error", "Planning failed"}};
}

