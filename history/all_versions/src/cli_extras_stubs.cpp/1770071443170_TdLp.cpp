bool APIServer::Start(uint16_t port) { 
    if (is_running_) return false;
    port_ = port;
    is_running_ = true;
    
    server_thread_ = std::make_unique<std::thread>([this]() {
         WSADATA wsaData;
         WSAStartup(MAKEWORD(2,2), &wsaData);
         
         SOCKET listenSock = socket(AF_INET, SOCK_STREAM, IPPROTO_TCP);
         sockaddr_in addr;
         addr.sin_family = AF_INET;
         addr.sin_addr.s_addr = INADDR_ANY;
         addr.sin_port = htons(port_);
         
         if (bind(listenSock, (sockaddr*)&addr, sizeof(addr)) == 0) {
             listen(listenSock, SOMAXCONN);
             std::cout << "[APIServer] Listening on " << port_ << " (WinSock/Real)\n";
             
             while(is_running_) {
                 fd_set readfds;
                 FD_ZERO(&readfds);
                 FD_SET(listenSock, &readfds);
                 struct timeval tv = {1, 0}; // 1s timeout
                 
                 int s = select(0, &readfds, NULL, NULL, &tv);
                 if (s > 0 && FD_ISSET(listenSock, &readfds)) {
                     SOCKET client = accept(listenSock, NULL, NULL);
                     if (client != INVALID_SOCKET) {
                         char buffer[4096];
                         int bytes = recv(client, buffer, 4096, 0);
                         if (bytes > 0) {
                             // Basic Response
                             std::string body = std::string("{\"status\":\"ok\", \"agent_mode\":\"") + (is_running_ ? "active" : "stopping") + "\"}";
                             std::string resp = "HTTP/1.1 200 OK\r\nContent-Type: application/json\r\nContent-Length: " + std::to_string(body.length()) + "\r\n\r\n" + body;
                             send(client, resp.c_str(), resp.length(), 0);
                         }
                         closesocket(client);
                     }
                 }
             }
         } else {
             std::cerr << "[APIServer] Bind failed\n";
         }
         closesocket(listenSock);
         WSACleanup();
    });
    return true; 
}

bool APIServer::Stop() { 
    is_running_ = false; 
    if(server_thread_ && server_thread_->joinable()) server_thread_->join();
    return true; 
}

