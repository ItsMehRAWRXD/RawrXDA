/// Helper: increment and return new value
inline uint64_t IncrementInference()    { return UTC_IncrementCounter(&g_Counter_Inference); }
inline uint64_t IncrementAgentLoop()    { return UTC_IncrementCounter(&g_Counter_AgentLoop); }
inline uint64_t IncrementScsiFails()    { return UTC_IncrementCounter(&g_Counter_ScsiFails); }
inline uint64_t IncrementBytePatches()  { return UTC_IncrementCounter(&g_Counter_BytePatches); }
inline uint64_t IncrementMemPatches()   { return UTC_IncrementCounter(&g_Counter_MemPatches); }
inline uint64_t IncrementServerPatches(){ return UTC_IncrementCounter(&g_Counter_ServerPatches); }
inline uint64_t IncrementErrors()       { return UTC_IncrementCounter(&g_Counter_Errors); }

/// Read all counters into a snapshot structure
struct MASMCounterSnapshot {
    uint64_t inference;
    uint64_t scsiFails;
    uint64_t agentLoop;
    uint64_t bytePatches;
    uint64_t memPatches;
    uint64_t serverPatches;
    uint64_t flushOps;
    uint64_t errors;
};

inline MASMCounterSnapshot ReadAllCounters() {
    MASMCounterSnapshot s;
    s.inference     = UTC_ReadCounter(&g_Counter_Inference);
    s.scsiFails     = UTC_ReadCounter(&g_Counter_ScsiFails);
    s.agentLoop     = UTC_ReadCounter(&g_Counter_AgentLoop);
    s.bytePatches   = UTC_ReadCounter(&g_Counter_BytePatches);
    s.memPatches    = UTC_ReadCounter(&g_Counter_MemPatches);
    s.serverPatches = UTC_ReadCounter(&g_Counter_ServerPatches);
    s.flushOps      = UTC_ReadCounter(&g_Counter_FlushOps);
    s.errors        = UTC_ReadCounter(&g_Counter_Errors);
    return s;
}

/// Generate Prometheus payload into a std::string-compatible buffer
inline size_t ExportPrometheusToBuffer(char* buf, size_t /*bufSize*/) {
    return static_cast<size_t>(ExportPrometheus(buf));
}

