// ============================================================================
// Telemetry Kernel вҖ” Atomic Counters (.data segment globals)
// ============================================================================
// Each counter is 64-byte aligned in the ASM .data segment to prevent
// false sharing across CPU cache lines. Access via lock xadd / lock inc.
// ============================================================================

/// Sentinel: start of metric table (value: 0xDEADBEEFCAFEBABE)
extern uint64_t g_MetricTableStart;

/// Total tokens generated by ASM inference kernels
extern uint64_t g_Counter_Inference;

/// Hardware SCSI timeout / failure count
extern uint64_t g_Counter_ScsiFails;

/// Total agentic loop steps executed
extern uint64_t g_Counter_AgentLoop;

/// Byte-level GGUF hotpatches applied
extern uint64_t g_Counter_BytePatches;

/// Memory-layer hotpatches applied (via VirtualProtect)
extern uint64_t g_Counter_MemPatches;

/// Server-layer request/response patches applied
extern uint64_t g_Counter_ServerPatches;

/// Ring buffer flush-to-disk operations
extern uint64_t g_Counter_FlushOps;

/// Total errors observed across all subsystems
extern uint64_t g_Counter_Errors;

/// Sentinel: end of metric table (value: 0xBADCAFEDEADFACE)
extern uint64_t g_MetricTableEnd;

// ============================================================================
// Telemetry Kernel вҖ” Lifecycle
// ============================================================================

/// Initialize telemetry subsystem: opens log file, resets counters.
/// @param logPath  Path to log file (null-terminated), or NULL for default
///                 ("rawrxd_kernel.log")
/// @return 0 on success, Win32 error code on failure
uint64_t UTC_InitTelemetry(const char* logPath);

/// Shut down telemetry: flush remaining events, close file handle.
/// @return 0 on success
uint64_t UTC_ShutdownTelemetry(void);

// ============================================================================
// Telemetry Kernel вҖ” Atomic Counter Operations
// ============================================================================

/// Thread-safe atomic increment via `lock xadd`.
/// Single cache-lock cycle вҖ” practically invisible to the profiler.
/// @param counterAddr  Pointer to a g_Counter_* global
/// @return New value after increment
uint64_t UTC_IncrementCounter(volatile uint64_t* counterAddr);

/// Thread-safe atomic decrement via `lock xadd`.
/// @param counterAddr  Pointer to a g_Counter_* global
/// @return New value after decrement
uint64_t UTC_DecrementCounter(volatile uint64_t* counterAddr);

/// Non-destructive atomic read with acquire fence (lfence).
/// @param counterAddr  Pointer to a g_Counter_* global
/// @return Current value
uint64_t UTC_ReadCounter(const volatile uint64_t* counterAddr);

/// Atomically exchange counter to zero and return old value.
/// @param counterAddr  Pointer to a g_Counter_* global
/// @return Previous value before reset
uint64_t UTC_ResetCounter(volatile uint64_t* counterAddr);

// ============================================================================
// Telemetry Kernel вҖ” Wait-Free Ring Buffer
// ============================================================================

/// Write a log event to the wait-free ring buffer.
/// Uses `lock xadd` for slot reservation вҖ” threads never stall.
/// @param message  Null-terminated ASCII string
/// @return Slot index used
uint64_t UTC_LogEvent(const char* message);

/// Drain ring buffer to the log file via direct Win32 WriteFile.
/// Single-consumer model: only one thread should call this.
/// @return Number of events flushed
uint64_t UTC_FlushToDisk(void);

/// Returns pointer to the metric table start sentinel.
/// Useful for walking the counter table programmatically.
/// @return Address of g_MetricTableStart
void* UTC_GetMetricTableBase(void);

/// Returns ring buffer head and tail indices (for diagnostics).
/// @return RAX = head index, RDX = tail index (use struct to capture both)
uint64_t UTC_GetEventBufferStats(void);

// ============================================================================
// Prometheus Exporter вҖ” Text Format Generation
// ============================================================================

/// Generate full Prometheus metrics payload into the provided buffer.
/// Format: Prometheus text exposition v0.0.4.
/// Buffer should be >= 8KB (recommended 64KB).
/// @param destBuffer  Destination buffer (caller-allocated)
/// @return Total bytes written (not including null terminator)
uint64_t ExportPrometheus(char* destBuffer);

/// Export a single metric line: "<name> <value>\n"
/// @param destBuffer  Destination buffer
/// @param metricName  Null-terminated metric name
/// @param counterPtr  Pointer to the uint64_t counter
/// @return Bytes written
uint64_t ExportPrometheusMetric(char* destBuffer, const char* metricName,
                                 const volatile uint64_t* counterPtr);

