// Local AI backend — runs inside D:\rawrxd, no C-drive refs
const express = require('express');
const app = express();
app.use(express.json());

// Code completions
app.post('/codewhisperer/generateCompletions', (req, res) => {
  res.json({
    completions: [{
      content: `// Generated by RawrXD local AI\nfunction ${req.body.context || 'example'}() {\n  return true;\n}`,
      references: []
    }]
  });
});

// Chat responses
app.post('/q/sendMessage', (req, res) => {
  res.json({
    conversationId: 'rawrxd-' + Date.now(),
    messageId: 'msg-' + Date.now(),
    message: `Local response to: ${req.body.message}`
  });
});

app.listen(3001, () => console.log('RawrXD Local AI on :3001'));
