# Save as: wire_sidebar.ps1
# Execute: .\wire_sidebar.ps1 -SourceDir "D:\rawrxd\src" -BuildConfig "Release"

param([string]$SourceDir=".", [string]$BuildConfig="Release")

Write-Host "[RawrXD] MASM64 Sidebar Auto-Wire v14.2" -ForegroundColor Cyan

# 1. Assemble the MASM64 source
$ml64 = "C:\Program Files (x86)\Microsoft Visual Studio\2022\BuildTools\VC\Tools\MSVC\14.40.33807\bin\Hostx64\x64\ml64.exe"
$masmSrc = "$SourceDir\win32app\Win32IDE_Sidebar.asm"
$objOut = "$SourceDir\win32app\Win32IDE_Sidebar_x64.obj"

Write-Host "[*] Assembling x64 native sidebar..." -ForegroundColor Yellow
& $ml64 /c /Fo "$objOut" "$masmSrc" /W3 /Zd /Zi 2>&1 | ForEach-Object { Write-Host $_ }

if ($LASTEXITCODE -ne 0) {
    Write-Host "[!] Assembly failed!" -ForegroundColor Red
    exit 1
}

# 2. Replace Qt object in CMake/Build system
$cmakeLists = Get-Content "$SourceDir\..\CMakeLists.txt" -Raw

# Remove Qt Sidebar
$cmakeLists = $cmakeLists -replace 'Win32IDE_Sidebar\.cpp', 'Win32IDE_Sidebar_x64.obj'
$cmakeLists = $cmakeLists -replace 'target_link_libraries\([^)]*Qt6::Widgets[^)]*\)', 'target_link_libraries(${PROJECT_NAME} PRIVATE comctl32 dwmapi shlwapi advapi32)'

Set-Content "$SourceDir\..\CMakeLists.txt" $cmakeLists
Write-Host "[+] CMake patched (Qt removed, Win32 API linked)" -ForegroundColor Green

# 3. Create header bridge for C++ integration
$bridgeHeader = @"
// SidebarBridge.h - Auto-generated by wire_sidebar.ps1
#pragma once
#include <Windows.h>

extern "C" {
    __declspec(dllimport) HWND Sidebar_Create(HWND hParent, const char* lpPath);
    __declspec(dllimport) void Sidebar_LogWrite(const char* msg, const char* file, int line);
    __declspec(dllimport) void DebugEngine_Loop(DWORD dwProcessId);
    __declspec(dllimport) int Git_Execute(const char* cmd, char* output, size_t outSize);
}

// Macro replacing qDebug/qInfo
#define RAWRXD_LOG(msg) Sidebar_LogWrite(msg, __FILE__, __LINE__)
"@

Set-Content "$SourceDir\win32app\SidebarBridge.h" $bridgeHeader
Write-Host "[+] Bridge header generated" -ForegroundColor Green

# 4. Patch MainWindow to use new sidebar
$mainWindowPath = "$SourceDir\win32app\Win32IDE.cpp"
if (Test-Path $mainWindowPath) {
    $mainWindow = Get-Content $mainWindowPath -Raw
    $mainWindow = $mainWindow -replace '#include.*Win32IDE_Sidebar\.h', '#include "SidebarBridge.h"'
    $mainWindow = $mainWindow -replace 'new Sidebar\(this\)', 'Sidebar_Create(this->winId(), "D:\\\\Projects")'
    $mainWindow = $mainWindow -replace 'qDebug\(\) <<', 'RAWRXD_LOG('

    Set-Content $mainWindowPath $mainWindow
    Write-Host "[+] Win32IDE.cpp patched" -ForegroundColor Green
}

# 5. Force dark mode registry (system-wide for RawrXD)
reg add "HKCU\Software\RawrXD\IDE" /v "ForceDarkMode" /t REG_DWORD /d 1 /f | Out-Null

Write-Host "[*] Build configuration complete. Execute:" -ForegroundColor Cyan
Write-Host "    cmake --build . --config $BuildConfig --target RawrXD-AgenticIDE" -ForegroundColor White
Write-Host "[+] Qt bloat eliminated: -2.3MB estimated" -ForegroundColor Green
