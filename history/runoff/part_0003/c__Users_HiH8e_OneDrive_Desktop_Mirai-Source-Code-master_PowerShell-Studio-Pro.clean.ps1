# Project Management
function New-PSProject {
  param(
    [string]$Name,
    [string]$Path,
    [ValidateSet('Script', 'GUI-WinForms', 'GUI-WPF', 'Module', 'Service')]
    [string]$Type = 'GUI-WinForms'
  )
    
  $projectPath = Join-Path $Path $Name
  New-Item -ItemType Directory -Path $projectPath -Force | Out-Null
    
  $project = [PSCustomObject]@{
    Name     = $Name
    Path     = $projectPath
    Type     = $Type
    MainFile = "$Name.ps1"
    Forms    = @()
    Modules  = @()
    Snippets = @()
    Created  = Get-Date
    Version  = "1.0.0"
    Author   = $env:USERNAME
  }
    
  $project | ConvertTo-Json -Depth 10 | Set-Content "$projectPath\project.json"
    
  # Create initial files
  switch ($Type) {
    'GUI-WinForms' {
      @"
# Generated by PowerShell Studio Pro
Add-Type -AssemblyName System.Windows.Forms
Add-Type -AssemblyName System.Drawing

# Form Designer Code
`$form = New-Object System.Windows.Forms.Form
`$form.Text = "$Name"
`$form.Size = New-Object System.Drawing.Size(800, 600)
`$form.StartPosition = "CenterScreen"

# Your code here

[void]`$form.ShowDialog()
"@ | Set-Content "$projectPath\$($project.MainFile)"
    }
    'GUI-WPF' {
      $wpfTemplate = @'
# Generated by PowerShell Studio Pro
Add-Type -AssemblyName PresentationFramework

[xml]$xaml = @"
<Window xmlns="http://schemas.microsoft.com/winfx/2006/xaml/presentation"
        Title="{0}" Height="600" Width="800">
    <Grid>
        <!-- Your controls here -->
    </Grid>
</Window>
"@

$reader = New-Object System.Xml.XmlNodeReader $xaml
$window = [Windows.Markup.XamlReader]::Load($reader)

$window.ShowDialog() | Out-Null
'@
      $wpfTemplate -f $Name | Set-Content "$projectPath\$($project.MainFile)"
    }
  }
    
  return $project
}

