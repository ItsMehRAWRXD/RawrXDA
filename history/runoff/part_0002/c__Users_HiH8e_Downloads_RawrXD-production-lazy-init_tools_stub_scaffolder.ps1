param(
    [string]$StubDir = (Join-Path $PSScriptRoot "..\bin\hexmag"),
    [string]$OutDir = $null
)

$ErrorActionPreference = "Stop"
if (-not $OutDir) {
    $OutDir = $StubDir
}

if (-not (Test-Path -LiteralPath $StubDir)) {
    Write-Host "Stub directory not found: $StubDir"
    exit 0
}

if (-not (Test-Path -LiteralPath $OutDir)) {
    New-Item -ItemType Directory -Path $OutDir | Out-Null
}

$stubFiles = Get-ChildItem -LiteralPath $StubDir -Filter "*_stubs.asm" -File -ErrorAction SilentlyContinue
if (-not $stubFiles) {
    Write-Host "No *_stubs.asm files found under $StubDir"
    exit 0
}

function Write-Scaffold {
    param(
        [System.IO.FileInfo]$StubFile
    )

    $names = New-Object System.Collections.Generic.List[string]
    foreach ($line in Get-Content -LiteralPath $StubFile.FullName) {
        if ($line -match '^\s*PUBLIC\s+([A-Za-z0-9_@\$\?]+)') {
            $names.Add($Matches[1])
        }
    }

    $base = [System.IO.Path]::GetFileNameWithoutExtension($StubFile.Name)
    $outPath = Join-Path $OutDir "$base`_scaffold.cpp"

    $writer = New-Object System.IO.StreamWriter($outPath, $false, [System.Text.Encoding]::ASCII)
    try {
        $writer.WriteLine("// Auto-generated scaffolding for $($StubFile.Name) - generated by tools/stub_scaffolder.ps1")
        $writer.WriteLine("// Set RAWRXD_STUB_LOG=1 to log stub invocations to terminal_diagnostics.log")
        $writer.WriteLine('#include "../../src/masm/stub_scaffold_support.hpp"')
        $writer.WriteLine("")
        foreach ($n in $names) {
            $writer.WriteLine("RAWRXD_DEFINE_STUB($n);")
        }
    }
    finally {
        $writer.Dispose()
    }

    Write-Host "Generated $outPath with $($names.Count) stubs"
}

foreach ($stub in $stubFiles) {
    Write-Scaffold -StubFile $stub
}
