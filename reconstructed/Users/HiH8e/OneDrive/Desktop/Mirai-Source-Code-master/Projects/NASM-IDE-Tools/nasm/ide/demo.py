import os
import json

def create_nasm_project():
    """Create a simple NASM project structure"""
    project_name = "hello_nasm"
    
    # Create directories
    os.makedirs(f"{project_name}/src", exist_ok=True)
    os.makedirs(f"{project_name}/build", exist_ok=True)
    os.makedirs(f"{project_name}/include", exist_ok=True)
    
    # Create main.asm - Hello World for Windows x64
    main_asm = '''
; Hello World for Windows x64 NASM
; Build: nasm -f win64 main.asm -o main.obj
; Link: link main.obj /subsystem:console /entry:main kernel32.lib

extern ExitProcess
extern GetStdHandle  
extern WriteConsoleA

section .data
    msg db 'Hello from NASM Assembly IDE!', 13, 10, 0
    msg_len equ $ - msg - 1
    STD_OUTPUT_HANDLE equ -11

section .bss
    bytes_written resd 1

section .text
    global main

main:
    ; Get stdout handle
    sub rsp, 40         ; Shadow space + alignment
    mov rcx, STD_OUTPUT_HANDLE
    call GetStdHandle
    mov rbx, rax        ; Save handle
    
    ; Write message to console
    mov rcx, rbx        ; Console handle
    mov rdx, msg        ; Buffer
    mov r8, msg_len     ; Number of chars
    mov r9, bytes_written ; Bytes written
    mov qword [rsp+32], 0   ; Reserved parameter
    call WriteConsoleA
    
    ; Exit cleanly
    mov rcx, 0          ; Exit code
    call ExitProcess
    add rsp, 40         ; Clean stack
    ret
'''
    
    with open(f"{project_name}/src/main.asm", 'w') as f:
        f.write(main_asm)
    
    # Create build script for Windows
    build_bat = '''@echo off
echo Building NASM Project...

REM Check if NASM is installed
nasm -version >nul 2>&1
if errorlevel 1 (
    echo ERROR: NASM not found! Please install NASM first.
    echo Download from: https://www.nasm.us/pub/nasm/releasebuilds/
    pause
    exit /b 1
)

REM Assemble
echo Assembling...
nasm -f win64 src/main.asm -o build/main.obj
if errorlevel 1 (
    echo Assembly failed!
    pause
    exit /b 1
)

REM Link (requires Visual Studio tools)
echo Linking...
link build/main.obj /subsystem:console /entry:main kernel32.lib /out:build/hello_nasm.exe
if errorlevel 1 (
    echo Linking failed! Make sure Visual Studio tools are in PATH.
    pause
    exit /b 1
)

echo Build successful! Executable: build/hello_nasm.exe
pause
'''
    
    with open(f"{project_name}/build.bat", 'w') as f:
        f.write(build_bat)
    
    # Create project config
    config = {
        "name": "hello_nasm",
        "version": "1.0.0",
        "description": "NASM Assembly Hello World Project",
        "main_file": "src/main.asm",
        "build_script": "build.bat",
        "target": "win64",
        "author": "Mirai Security Toolkit"
    }
    
    with open(f"{project_name}/project.json", 'w') as f:
        json.dump(config, f, indent=2)
    
    # Create README
    readme = f'''# {project_name.upper()} - NASM Assembly Project

## Description
Simple Hello World program written in NASM Assembly for Windows x64.

## Requirements
- NASM Assembler (https://www.nasm.us/)
- Visual Studio Build Tools or Visual Studio (for linker)

## Build Instructions
1. Run `build.bat` to compile and link
2. Execute `build/hello_nasm.exe`

## Files
- `src/main.asm` - Main assembly source
- `build.bat` - Build script
- `project.json` - Project configuration

## NASM IDE Features Demonstrated
✅ Windows x64 target
✅ External function calls (Win32 API)
✅ String data handling
✅ Console output
✅ Clean program exit
✅ Proper calling conventions
✅ Stack management

Generated by Mirai Security Toolkit NASM IDE Converter
'''
    
    with open(f"{project_name}/README.md", 'w') as f:
        f.write(readme)
    
    return project_name

