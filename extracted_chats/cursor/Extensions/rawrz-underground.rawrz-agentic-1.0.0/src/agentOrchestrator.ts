import * as vscode from 'vscode';

interface ModelResponse {
    completion: string;
    confidence?: number;
    model: string;
    tokens_used?: number;
}

interface GenerationRequest {
    prompt: string;
    model: string;
    temperature?: number;
    max_tokens?: number;
    task?: string;
    language?: string;
}

export class AgentOrchestrator {
    private modelEndpoint: string;
    private primaryModel: string;
    private secondaryModel: string;
    private cache: Map<string, string> = new Map();

    constructor() {
        const config = vscode.workspace.getConfiguration('rawrz');
        this.modelEndpoint = config.get('model.endpoint', 'http://localhost:11438/generate');
        this.primaryModel = config.get('models.primary', 'your-custom-model');
        this.secondaryModel = config.get('models.secondary', 'mock-model');
    }

    async generateCode(code: string, languageId: string, context: string, task: string): Promise<string> {
        const agenticMode = vscode.workspace.getConfiguration('rawrz.agentic').get('mode', 'collaborative') as string;
        
        // Check cache first
        const cacheKey = this.generateCacheKey(code, languageId, task, context);
        const cached = this.cache.get(cacheKey);
        if (cached) {
            console.log('RawrZ: Using cached generation');
            return cached;
        }

        const prompt = this.buildPrompt(code, languageId, context, task, agenticMode);
        
        let completion: string;
        
        if (agenticMode === 'autonomous') {
            completion = await this.autonomousGeneration(prompt, languageId);
        } else if (agenticMode === 'collaborative') {
            completion = await this.collaborativeGeneration(prompt, languageId);
        } else if (agenticMode === 'paranoid') {
            completion = await this.paranoidGeneration(prompt, languageId);
        } else {
            // Default to collaborative mode
            completion = await this.collaborativeGeneration(prompt, languageId);
        }

        // Cache the result
        this.cache.set(cacheKey, completion);
        return completion;
    }

    async explainCode(code: string, languageId: string): Promise<string> {
        const prompt = `# CODE EXPLANATION TASK

Explain the following ${languageId} code in detail. Be thorough and practical.

## CODE TO EXPLAIN:
\`\`\`${languageId}
${code}
\`\`\`

## EXPLANATION REQUIREMENTS:
- What the code does functionally
- Key algorithms, patterns, or techniques used
- Potential performance characteristics
- Error handling approach
- Security considerations
- Potential improvements or optimizations
- Any edge cases to consider

## FORMAT:
Provide a clear, structured explanation with sections if needed.`;

        return await this.callModel(prompt, this.secondaryModel, 'explain');
    }

    async refactorCode(code: string, languageId: string, context: string): Promise<string> {
        const prompt = this.buildPrompt(code, languageId, context, 'refactor', 'collaborative');
        return await this.collaborativeGeneration(prompt, languageId);
    }

    private async autonomousGeneration(prompt: string, languageId: string): Promise<string> {
        // Direct generation with primary model - no review
        return await this.callModel(prompt, this.primaryModel, 'generate');
    }

    private async collaborativeGeneration(prompt: string, languageId: string): Promise<string> {
        try {
            // Generate with primary model
            const primaryResponse = await this.callModel(prompt, this.primaryModel, 'generate');
            
            // Have secondary model review and improve
            const reviewPrompt = `# CODE REVIEW & IMPROVEMENT TASK

Review and improve the following ${languageId} code generated by another AI model.

## ORIGINAL TASK:
${prompt}

## GENERATED CODE:
\`\`\`${languageId}
${primaryResponse}
\`\`\`

## REVIEW INSTRUCTIONS:
1. Analyze the code for correctness, efficiency, and style
2. Identify any bugs, inefficiencies, or style issues
3. Suggest specific improvements
4. Provide the improved version with comments explaining changes
5. Maintain the original functionality

## IMPROVED CODE:`;

            const reviewResponse = await this.callModel(reviewPrompt, this.secondaryModel, 'review');
            
            // Extract just the code from review response
            return this.extractCodeFromResponse(reviewResponse, languageId);
        } catch (error) {
            // Fallback to primary model if collaborative fails
            console.warn('Collaborative generation failed, using primary model:', error);
            return await this.callModel(prompt, this.primaryModel, 'generate');
        }
    }

    private async paranoidGeneration(prompt: string, languageId: string): Promise<string> {
        // Generate with both models and return the more conservative option
        const [primaryResponse, secondaryResponse] = await Promise.all([
            this.callModel(prompt, this.primaryModel, 'generate'),
            this.callModel(prompt, this.secondaryModel, 'generate')
        ]);

        // Choose the shorter, more conservative response
        return primaryResponse.length < secondaryResponse.length ? primaryResponse : secondaryResponse;
    }

    private async callModel(prompt: string, model: string, task: string): Promise<string> {
        // Try BigDaddyG server first (port 11441)
        try {
            const bigdaddygRequest = {
                prompt,
                model,
                temperature: this.getTemperatureForTask(task),
                max_tokens: this.getMaxTokensForTask(task)
            };

            const response = await fetch('http://localhost:11441/v1/completions', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(bigdaddygRequest)
            });

            if (response.ok) {
                const result = await response.json() as any;
                if (result.choices && result.choices[0]) {
                    this.logModelUsage(model, task, result.usage?.total_tokens);
                    return result.choices[0].text;
                }
            }
        } catch (error) {
            console.warn('BigDaddyG server failed, trying fallback:', error);
        }

        // Fallback to RawrZ server (port 11438)
        try {
            const request: GenerationRequest = {
                prompt,
                model: 'mock-model', // Use mock model as fallback
                task,
                temperature: this.getTemperatureForTask(task),
                max_tokens: this.getMaxTokensForTask(task)
            };

            const response = await fetch('http://localhost:11438/generate', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(request)
            });

            if (!response.ok) {
                throw new Error(`Model server error: ${response.statusText}`);
            }

            const result = await response.json() as ModelResponse;
            
            // Log usage for analytics
            this.logModelUsage(model, task, result.tokens_used);
            
            return result.completion;
        } catch (error) {
            throw new Error(`All model servers failed: ${error}`);
        }
    }

    private buildPrompt(code: string, languageId: string, context: string, task: string, mode: string): string {
        const contextSection = context ? `## RELEVANT CODE CONTEXT:
The following code snippets from your codebase are relevant to this task:

${context}

` : '';

        const modeInstructions = this.getModeInstructions(mode);

        return `# ${task.toUpperCase()} TASK
${modeInstructions}
## PROGRAMMING LANGUAGE: ${languageId.toUpperCase()}
${contextSection}## USER CODE:
\`\`\`${languageId}
${code}
\`\`\`

## TASK INSTRUCTIONS:
${this.getTaskInstructions(task)}

## RESPONSE REQUIREMENTS:
- Generate clean, efficient, and idiomatic ${languageId} code
- Follow the patterns and style from the context if provided
- Include appropriate error handling and validation
- Add comments for complex logic or important decisions
- Ensure code is production-ready and secure
- Return ONLY the code, no explanations or markdown formatting

## GENERATED CODE:`;
    }

    private getModeInstructions(mode: string): string {
        switch (mode) {
            case 'autonomous':
                return '**MODE: AUTONOMOUS** - Apply best practices and make confident improvements.';
            case 'collaborative':
                return '**MODE: COLLABORATIVE** - Balance innovation with safety, provide well-reasoned code.';
            case 'paranoid':
                return '**MODE: PARANOID** - Be conservative, minimal changes, maximum safety.';
            default:
                return '**MODE: STANDARD** - Provide high-quality, safe code improvements.';
        }
    }

    private getTaskInstructions(task: string): string {
        const instructions: { [key: string]: string } = {
            'generate': 'Generate new code that completes or enhances the provided code. Focus on functionality, efficiency, and maintainability.',
            'refactor': 'Refactor the code to improve structure, readability, and performance without changing behavior.',
            'explain': 'Provide a detailed explanation of the code functionality, patterns, and potential improvements.',
            'debug': 'Identify and fix any issues in the code. Explain what was wrong and how you fixed it.',
            'test': 'Generate comprehensive tests for the provided code. Include edge cases and error conditions.'
        };
        
        return instructions[task] || 'Complete the requested coding task with high quality and attention to detail.';
    }

    private getTemperatureForTask(task: string): number {
        const temperatures: { [key: string]: number } = {
            'generate': 0.7,
            'refactor': 0.3,
            'explain': 0.5,
            'debug': 0.2,
            'review': 0.4,
            'test': 0.3
        };
        
        return temperatures[task] || 0.5;
    }

    private getMaxTokensForTask(task: string): number {
        const tokens: { [key: string]: number } = {
            'generate': 2000,
            'refactor': 1500,
            'explain': 1000,
            'debug': 1200,
            'review': 1800,
            'test': 1500
        };
        
        return tokens[task] || 1500;
    }

    private extractCodeFromResponse(response: string, languageId: string): string {
        // Try to extract code from markdown code blocks first
        const codeBlockRegex = new RegExp(`\`\`\`(?:${languageId})?\\s*\\n([\\s\\S]*?)\`\`\``, 'i');
        const match = response.match(codeBlockRegex);
        
        if (match) {
            return match[1].trim();
        }
        
        // If no code blocks, look for code between tags
        const tagsRegex = /(?:```|%%%|~~~)([\s\S]*?)(?:```|%%%|~~~)/;
        const tagsMatch = response.match(tagsRegex);
        
        if (tagsMatch) {
            return tagsMatch[1].trim();
        }
        
        // Otherwise, return the entire response but clean it up
        return response.replace(/^(```\w*\n|\n```)$/gm, '').trim();
    }

    private generateCacheKey(code: string, languageId: string, task: string, context: string): string {
        // Simple hash-based cache key
        const content = `${code}|${languageId}|${task}|${context}`;
        let hash = 0;
        for (let i = 0; i < content.length; i++) {
            const char = content.charCodeAt(i);
            hash = ((hash << 5) - hash) + char;
            hash = hash & hash; // Convert to 32-bit integer
        }
        return `rawrz_${Math.abs(hash)}`;
    }

    private logModelUsage(model: string, task: string, tokensUsed?: number): void {
        console.log(`RawrZ: Model ${model} used for ${task} task, tokens: ${tokensUsed || 'unknown'}`);
    }

    // Clear cache - useful for development
    clearCache(): void {
        this.cache.clear();
        console.log('RawrZ: Generation cache cleared');
    }
}