class ProjectTemplateGenerator:
    """
    Generates complete project structures
    Integrates with your existing file management system
    """
    
    def __init__(self, ide_instance, code_generator: RealCodeGenerator):
        self.ide = ide_instance
        self.code_gen = code_generator
    
    def create_project(self, name: str, project_type: str, language: str, output_dir: str) -> bool:
        """Create a complete project structure"""
        
        try:
            project_path = Path(output_dir) / name
            project_path.mkdir(parents=True, exist_ok=True)
            
            if project_type == "console_app":
                return self.create_console_project(name, language, project_path)
            elif project_type == "gui_app":
                return self.create_gui_project(name, language, project_path)
            elif project_type == "web_service":
                return self.create_web_project(name, language, project_path)
            else:
                return self.create_basic_project(name, language, project_path)
                
        except Exception as e:
            if hasattr(self.ide, 'log_message'):
                self.ide.log_message(f"Project creation failed: {e}")
            return False
    
    def create_console_project(self, name: str, language: str, project_path: Path) -> bool:
        """Create console application project"""
        
        # Main source file
        if language == "python":
            main_file = project_path / f"{name.lower()}.py"
            code = self.code_gen.templates["console_app"]["python"].format(name=name)
        elif language == "eon":
            main_file = project_path / f"{name.lower()}.eon"
            code = self.code_gen.templates["console_app"]["eon"].format(name=name)
        elif language in ["cpp", "c++"]:
            main_file = project_path / "main.cpp"
            code = self.code_gen.templates["console_app"]["cpp"].format(name=name)
        else:
            main_file = project_path / f"main.{language}"
            code = f"// {name} - Console Application\\n// Generated by Enhanced Code Generator\\n"
        
        main_file.write_text(code, encoding='utf-8')
        
        # README
        readme_content = f"""# {name}

Console application generated by Enhanced Code Generator.

## Build and Run

### {language.capitalize()}
"""
        
        if language == "python":
            readme_content += f"""
```bash
python {name.lower()}.py
```"""
        elif language == "eon":
            readme_content += f"""
```bash
# Compile with EON compiler
eon compile {name.lower()}.eon
./{name.lower()}
```"""
        elif language in ["cpp", "c++"]:
            readme_content += f"""
```bash
g++ -o {name.lower()} main.cpp
./{name.lower()}
```"""
        
        (project_path / "README.md").write_text(readme_content, encoding='utf-8')
        
        return True
    
