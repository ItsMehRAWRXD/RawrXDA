    def generate_from_description(self, description: str, language: str = "python") -> str:
        """
        Generate code from natural language description
        Uses your existing AI system if available
        """
        
        if self.ai_available and hasattr(self.ide.ai_system, 'generate_code'):
            # Use your existing AI integration
            try:
                prompt = f"Generate {language} code for: {description}"
                ai_result = self.ide.ai_system.generate_code(prompt)
                if ai_result and ai_result.strip():
                    return ai_result
            except Exception as e:
                print(f"AI generation failed, using templates: {e}")
        
        # Fallback to template-based generation
        return self.generate_from_template(description, language)
    
    def generate_from_template(self, description: str, language: str = "python") -> str:
        """Template-based code generation as fallback"""
        
        # Analyze description to determine template type
        template_type = self.detect_template_type(description)
        
        if template_type in self.templates and language in self.templates[template_type]:
            # Extract project name from description
            name = self.extract_name_from_description(description)
            class_name = self.to_class_name(name)
            
            template = self.templates[template_type][language]
            return template.format(name=name, class_name=class_name)
        
        # Basic code generation
        return self.generate_basic_code(description, language)
    
    def detect_template_type(self, description: str) -> str:
        """Detect what type of application to generate"""
        description_lower = description.lower()
        
        if any(word in description_lower for word in ["web", "server", "api", "http", "rest"]):
            return "web_service"
        elif any(word in description_lower for word in ["gui", "window", "interface", "ui", "tkinter"]):
            return "gui_app"
        else:
            return "console_app"
    
    def extract_name_from_description(self, description: str) -> str:
        """Extract project name from description"""
        
        # Look for "called X" or "named X"
        name_patterns = [
            r"called\s+([a-zA-Z_][a-zA-Z0-9_]*)",
            r"named\s+([a-zA-Z_][a-zA-Z0-9_]*)",
            r"create\s+([a-zA-Z_][a-zA-Z0-9_]*)",
            r"make\s+([a-zA-Z_][a-zA-Z0-9_]*)"
        ]
        
        for pattern in name_patterns:
            match = re.search(pattern, description, re.IGNORECASE)
            if match:
                return match.group(1)
        
        # Default name
        return "MyApplication"
    
    def to_class_name(self, name: str) -> str:
        """Convert name to proper class name"""
        # Remove non-alphanumeric characters and convert to PascalCase
        clean_name = re.sub(r'[^a-zA-Z0-9]', '', name)
