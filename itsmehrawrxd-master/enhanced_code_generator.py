    from tkinter import ttk, messagebox, filedialog
    
    dialog = tk.Toplevel(ide_instance.root if hasattr(ide_instance, 'root') else None)
    dialog.title("Create New Project")
    dialog.geometry("500x350")
    
    # Project name
    ttk.Label(dialog, text="Project Name:").pack(pady=5)
    name_var = tk.StringVar(value="MyProject")
    name_entry = ttk.Entry(dialog, textvariable=name_var, width=50)
    name_entry.pack(pady=5)
    
    # Project type
    ttk.Label(dialog, text="Project Type:").pack(pady=5)
    type_var = tk.StringVar(value="console_app")
    type_combo = ttk.Combobox(dialog, textvariable=type_var, 
                             values=["console_app", "gui_app", "web_service"])
    type_combo.pack(pady=5)
    
    # Language
    ttk.Label(dialog, text="Language:").pack(pady=5)
    lang_var = tk.StringVar(value="python")
    lang_combo = ttk.Combobox(dialog, textvariable=lang_var, 
                             values=["python", "eon", "cpp", "javascript"])
    lang_combo.pack(pady=5)
    
    # Output directory
    ttk.Label(dialog, text="Output Directory:").pack(pady=5)
    dir_var = tk.StringVar(value=os.getcwd())
    dir_frame = ttk.Frame(dialog)
    dir_frame.pack(pady=5, fill='x', padx=10)
    dir_entry = ttk.Entry(dir_frame, textvariable=dir_var)
    dir_entry.pack(side='left', fill='x', expand=True)
    
    def browse_dir():
        directory = filedialog.askdirectory(initialdir=dir_var.get())
        if directory:
            dir_var.set(directory)
    
    ttk.Button(dir_frame, text="Browse", command=browse_dir).pack(side='right', padx=(5,0))
    
    def create_project():
        try:
            success = ide_instance.project_generator.create_project(
                name_var.get(), type_var.get(), lang_var.get(), dir_var.get()
            )
            if success:
                messagebox.showinfo("Success", f"Project '{name_var.get()}' created successfully!")
                dialog.destroy()
            else:
                messagebox.showerror("Error", "Failed to create project")
        except Exception as e:
            messagebox.showerror("Error", f"Project creation failed: {e}")
    
    # Buttons
    button_frame = ttk.Frame(dialog)
    button_frame.pack(pady=20)
    
    ttk.Button(button_frame, text="Create Project", command=create_project).pack(side='left', padx=5)
    ttk.Button(button_frame, text="Cancel", command=dialog.destroy).pack(side='left', padx=5)

