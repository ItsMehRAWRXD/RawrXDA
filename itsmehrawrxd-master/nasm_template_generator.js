// Generate the complete assembly file
function generateEONAssemblyIDE() {
    let output = [];
    
    // Header section
    output.push(generateHeader());
    output.push('');
    
    // External declarations
    output.push(generateExterns());
    output.push('');
    
    // Constants and definitions
    output.push(generateConstants());
    output.push('');
    
    // Data sections
    output.push(generateDataSections());
    output.push('');
    
    // BSS sections
    output.push(generateBSSSections());
    output.push('');
    
    // Text section with all functions
    output.push(generateTextSection());
    
    return output.join('\n');
}

