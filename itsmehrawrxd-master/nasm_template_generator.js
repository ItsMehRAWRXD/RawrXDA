function generateTextSection() {
    let text = [
        'section .text',
        'global _start',
        '',
        '_start:',
        '    ; Initialize the IDE',
        '    call init_ide',
        '    call show_welcome',
        '    call main_loop',
        '    call cleanup_ide',
        '    call exit_program',
        '',
        '; ===================================================================',
        '; INITIALIZATION AND CLEANUP',
        '; ==================================================================='
    ];
    
    // Add initialization functions
    text.push(generateInitFunctions());
    text.push('');
    
    // Add all module functions
    Object.keys(TEMPLATE_STRUCTURE.modules).forEach(moduleName => {
        text.push(`; ===================================================================`);
        text.push(`; ${moduleName.toUpperCase()} MODULE`);
        text.push(`; ===================================================================`);
        text.push(generateModuleFunctions(moduleName));
        text.push('');
    });
    
    // Add utility functions
    text.push('; ===================================================================');
    text.push('; UTILITY FUNCTIONS');
    text.push('; ===================================================================');
    text.push(generateUtilityFunctions());
    
    return text.join('\n');
}

function generateInitFunctions() {
    return `init_ide:
    ; Initialize the EON IDE
    push rbp
    mov rbp, rsp
    
    ; Initialize editor state
    mov qword [editor_buffer_size], 0
    mov dword [cursor_x], 0
    mov dword [cursor_y], 0
    mov qword [selection_start], 0
    mov qword [selection_end], 0
    
    ; Initialize file state
    mov byte [file_modified], 0
    mov byte [file_saved], 1
    
    ; Initialize undo system
    mov dword [undo_levels], 0
    mov dword [current_undo], 0
    
    ; Initialize project state
    mov dword [project_file_count], 0
    
    ; Initialize compiler state
    mov byte [build_success], 0
    
    ; Initialize debugger state
    mov dword [breakpoint_count], 0
    mov byte [debug_running], 0
    
    ; Initialize UI state
    mov dword [window_width], WIN_WIDTH
    mov dword [window_height], WIN_HEIGHT
    mov dword [mouse_x], 0
    mov dword [mouse_y], 0
    mov dword [mouse_buttons], 0
    
    ; Initialize XCB connection (if GUI enabled)
    call init_xcb_connection
    
    pop rbp
    ret

init_xcb_connection:
    ; Initialize XCB connection for GUI
    push rbp
    mov rbp, rsp
    
    ; Connect to X server
    xor rdi, rdi
    xor rsi, rsi
    call xcb_connect
    mov [xcb_connection], rax
    
    ; Get setup and screen
    mov rdi, rax
    call xcb_get_setup
    mov rdi, rax
    call xcb_setup_roots_iterator
    mov [xcb_screen], rax
    
    ; Generate window ID
    mov rdi, [xcb_connection]
    call xcb_generate_id
    mov [xcb_window], eax
    
    ; Create window
    call create_main_window
    
    ; Create graphics context
    mov rdi, [xcb_connection]
    call xcb_generate_id
    mov [xcb_gc], eax
    call create_graphics_context
    
    pop rbp
    ret

show_welcome:
    ; Display welcome message
    push rbp
    mov rbp, rsp
    
    lea rsi, [welcome_msg]
    call write_string
    
    lea rsi, [version_info]
    call write_string
    
    lea rsi, [help_text]
    call write_string
    
    pop rbp
    ret

main_loop:
    ; Main IDE event loop
    push rbp
    mov rbp, rsp
    
.main_loop_start:
    ; Check for events
    call process_events
    
    ; Update UI
    call update_ui
    
    ; Render display
    call render_display
    
    ; Check for exit condition
    call check_exit_condition
    test rax, rax
    jnz .main_loop_exit
    
    jmp .main_loop_start
    
.main_loop_exit:
    pop rbp
    ret

cleanup_ide:
    ; Cleanup IDE resources
    push rbp
    mov rbp, rsp
    
    ; Disconnect XCB
    mov rdi, [xcb_connection]
    call xcb_disconnect
    
    ; Free allocated memory
    call free_allocated_memory
    
    pop rbp
    ret

exit_program:
    ; Exit the program
    mov rax, SYS_exit
    xor rdi, rdi
    syscall`;
}

function generateModuleFunctions(moduleName) {
    const module = TEMPLATE_STRUCTURE.modules[moduleName];
    let functions = [];
    
    module.functions.forEach(funcName => {
        functions.push(generateFunction(funcName, moduleName));
        functions.push('');
    });
    
    return functions.join('\n');
}

function generateFunction(funcName, moduleName) {
    const functionTemplates = {
        editor: {
            insert_char: `insert_char:
    ; Insert character at cursor position
    ; Input: AL = character to insert
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    ; Get buffer pointer and cursor position
    mov rdi, [editor_buffer]
    mov esi, [cursor_x]
    mov edx, [cursor_y]
    
    ; Calculate linear position (assuming 80 chars per line)
    mov eax, edx
    mov ecx, 80
    mul ecx
    add eax, esi
    add rdi, rax
    
    ; Shift characters right to make space
    mov rcx, [editor_buffer_size]
    sub rcx, rax
    jz .no_shift
    
    mov rsi, rdi
    add rsi, rcx
    mov rdx, rdi
    add rdx, rcx
    inc rdx
    
    std
    rep movsb
    cld
    
.no_shift:
    ; Insert the character
    mov [rdi], al
    
    ; Update cursor position
    inc dword [cursor_x]
    
    ; Update buffer size
    inc qword [editor_buffer_size]
    
    ; Mark file as modified
    mov byte [file_modified], 1
    
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret`,
            
            delete_char: `delete_char:
    ; Delete character at cursor position
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    ; Check if at end of buffer
    mov rax, [cursor_x]
    mov rbx, [cursor_y]
    mov rcx, 80
    mul rcx
    add rax, rbx
    cmp rax, [editor_buffer_size]
    jge .delete_done
    
    ; Get buffer pointer
    lea rdi, [editor_buffer]
    add rdi, rax
    
    ; Shift characters left
    mov rsi, rdi
    inc rsi
    mov rcx, [editor_buffer_size]
    sub rcx, rax
    dec rcx
    jz .delete_done
    
    cld
    rep movsb
    
    ; Update buffer size
    dec qword [editor_buffer_size]
    
    ; Mark file as modified
    mov byte [file_modified], 1
    
.delete_done:
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret`
        },
        
        file_ops: {
            open_file: `open_file:
    ; Open file for editing
    ; Input: RSI = filename pointer
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    ; Copy filename to current_file
    lea rdi, [current_file]
    mov rcx, 255
    cld
    rep movsb
    mov byte [rdi], 0
    
    ; Open file
    lea rdi, [current_file]
    mov rax, SYS_open
    mov rsi, O_RDONLY
    xor rdx, rdx
    syscall
    
    cmp rax, 0
    jl .open_error
    
    mov rbx, rax  ; Save file descriptor
    
    ; Read file into editor buffer
    lea rsi, [editor_buffer]
    mov rdx, MAX_BUFFER_SIZE
    mov rax, SYS_read
    syscall
    
    cmp rax, 0
    jl .read_error
    
    mov [editor_buffer_size], rax
    
    ; Close file
    mov rax, SYS_close
    mov rdi, rbx
    syscall
    
    ; Reset cursor and mark as saved
    mov dword [cursor_x], 0
    mov dword [cursor_y], 0
    mov byte [file_modified], 0
    mov byte [file_saved], 1
    
    jmp .open_done
    
.open_error:
    lea rsi, [error_file_not_found]
    call write_string
    jmp .open_done
    
.read_error:
    lea rsi, [error_permission]
    call write_string
    
.open_done:
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret`
        },
        
        compiler: {
            lex_analyze: `lex_analyze:
    ; Lexical analysis of source code
    ; Input: RSI = source code pointer, RCX = length
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    ; Initialize lexer state
    mov [lexer_state + 0], rsi    ; source pointer
    mov [lexer_state + 8], rcx    ; source length
    mov qword [lexer_state + 16], 0  ; current position
    mov qword [lexer_state + 24], 0  ; token count
    
    ; Tokenize the source
    call tokenize_source
    
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret`,
            
            parse_syntax: `parse_syntax:
    ; Parse syntax tree from tokens
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    ; Initialize parser state
    mov qword [parser_state + 0], 0  ; token index
    mov qword [parser_state + 8], 0  ; syntax tree root
    mov qword [parser_state + 16], 0 ; error count
    
    ; Parse tokens into syntax tree
    call build_syntax_tree
    
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret`
        }
    };
    
    // Get function template based on module and function name
    const moduleTemplates = functionTemplates[moduleName];
    if (moduleTemplates && moduleTemplates[funcName]) {
        return moduleTemplates[funcName];
    }
    
    // Default function template
    return `${funcName}:
    ; ${funcName} - ${moduleName} function
    push rbp
    mov rbp, rsp
    
    ; TODO: Implement ${funcName}
    ; This is a placeholder for the full implementation
    
    pop rbp
    ret`;
}

function generateUtilityFunctions() {
    return `write_string:
    ; Write null-terminated string to stdout
    ; Input: RSI = string pointer
    push rbp
    mov rbp, rsp
    push rdi
    push rdx
    
    ; Calculate string length
    mov rdi, rsi
    xor rcx, rcx
.len_loop:
    mov al, [rdi]
    cmp al, 0
    je .len_done
    inc rcx
    inc rdi
    jmp .len_loop
.len_done:
    
    ; Write string
    mov rdx, rcx
    mov rax, SYS_write
    mov rdi, STDOUT
    syscall
    
    pop rdx
    pop rdi
    pop rbp
    ret

write_buffer:
    ; Write buffer to stdout
    ; Input: RSI = buffer pointer, RDX = length
    push rbp
    mov rbp, rsp
    push rdi
    
    mov rax, SYS_write
    mov rdi, STDOUT
    syscall
    
    pop rdi
    pop rbp
    ret

read_char:
    ; Read single character from stdin
    ; Output: AL = character
    push rbp
    mov rbp, rsp
    push rdi
    push rsi
    push rdx
    
    lea rsi, [temp_buffer]
    mov rdx, 1
    mov rax, SYS_read
    mov rdi, STDIN
    syscall
    
    cmp rax, 1
    jne .read_fail
    movzx rax, byte [temp_buffer]
    jmp .read_done
    
.read_fail:
    xor rax, rax
    
.read_done:
    pop rdx
    pop rsi
    pop rdi
    pop rbp
    ret

allocate_memory:
    ; Allocate memory block
    ; Input: RDI = size in bytes
    ; Output: RAX = pointer to allocated memory
    push rbp
    mov rbp, rsp
    
    call malloc
    
    pop rbp
    ret

free_memory:
    ; Free allocated memory
    ; Input: RDI = pointer to memory
    push rbp
    mov rbp, rsp
    
    call free
    
    pop rbp
    ret

copy_string:
    ; Copy null-terminated string
    ; Input: RSI = source, RDI = destination
    push rbp
    mov rbp, rsp
    push rcx
    
    xor rcx, rcx
.copy_loop:
    mov al, [rsi]
    mov [rdi], al
    cmp al, 0
    je .copy_done
    inc rsi
    inc rdi
    inc rcx
    jmp .copy_loop
    
.copy_done:
    pop rcx
    pop rbp
    ret

compare_strings:
    ; Compare two null-terminated strings
    ; Input: RSI = string1, RDI = string2
    ; Output: RAX = 0 if equal, non-zero if different
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    
    xor rcx, rcx
.compare_loop:
    mov al, [rsi]
    mov bl, [rdi]
    cmp al, bl
    jne .compare_different
    cmp al, 0
    je .compare_equal
    inc rsi
    inc rdi
    inc rcx
    jmp .compare_loop
    
.compare_equal:
    xor rax, rax
    jmp .compare_done
    
.compare_different:
    mov rax, 1
    
.compare_done:
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret

int_to_string:
    ; Convert integer to string
    ; Input: RDI = integer value, RSI = buffer pointer
    ; Output: RAX = string length
    push rbp
    mov rbp, rsp
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    
    mov rax, rdi
    mov rdi, rsi
    add rdi, 31  ; Start from end of buffer
    mov byte [rdi], 0
    dec rdi
    
    ; Handle zero case
    cmp rax, 0
    jne .convert_loop
    mov byte [rdi], '0'
    mov rax, 1
    jmp .convert_done
    
.convert_loop:
    xor rdx, rdx
    mov rbx, 10
    div rbx
    add dl, '0'
    mov [rdi], dl
    dec rdi
    cmp rax, 0
    jne .convert_loop
    
    inc rdi
    mov rax, rdi
    sub rax, rsi
    neg rax
    add rax, 32
    
.convert_done:
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rbp
    ret

process_events:
    ; Process system events
    push rbp
    mov rbp, rsp
    
    ; Check for keyboard input
    call check_keyboard_input
    
    ; Check for mouse input (if GUI enabled)
    call check_mouse_input
    
    ; Check for file system events
    call check_file_events
    
    pop rbp
    ret

update_ui:
    ; Update user interface
    push rbp
    mov rbp, rsp
    
    ; Update editor display
    call update_editor_display
    
    ; Update status bar
    call update_status_bar
    
    ; Update file tree
    call update_file_tree
    
    pop rbp
    ret

render_display:
    ; Render the display
    push rbp
    mov rbp, rsp
    
    ; Clear screen
    lea rsi, [clear_screen]
    call write_string
    
    ; Draw UI components
    call draw_menu_bar
    call draw_toolbar
    call draw_editor_area
    call draw_status_bar
    
    pop rbp
    ret

check_exit_condition:
    ; Check if IDE should exit
    ; Output: RAX = 1 if should exit, 0 if continue
    push rbp
    mov rbp, rsp
    
    ; Check for quit command
    lea rsi, [command_buffer]
    lea rdi, [cmd_quit]
    call compare_strings
    cmp rax, 0
    je .should_exit
    
    ; Check for Ctrl+C
    call check_keyboard_input
    cmp al, 3  ; Ctrl+C
    je .should_exit
    
    xor rax, rax
    jmp .check_done
    
.should_exit:
    mov rax, 1
    
.check_done:
    pop rbp
    ret

; Command strings
cmd_quit db ':quit', 0
cmd_help db ':help', 0
cmd_new  db ':new', 0
cmd_open db ':open', 0
cmd_save db ':save', 0
cmd_build db ':build', 0
cmd_run  db ':run', 0
cmd_debug db ':debug', 0`;
}

