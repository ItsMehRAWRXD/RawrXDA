// RawrZ Java + .NET Unified Desktop Cryptor - Cross-Platform Encryption Engine
const crypto = require('crypto');
const { logger } = require('../utils/logger');
const fs = require('fs').promises;
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

class JavaDotNetUnifiedCryptor {
    constructor() {
        this.name = 'JavaDotNetUnifiedCryptor';
        this.version = '2.0.0';
        this.initialized = false;
        
        // Java integration
        this.javaAvailable = false;
        this.javaVersion = null;
        this.javaCryptorPath = path.join(__dirname, '../templates/java/RawrZJavaCryptor.java');
        
        // .NET integration  
        this.dotnetAvailable = false;
        this.dotnetVersion = null;
        this.dotnetCryptorPath = path.join(__dirname, '../templates/dotnet/RawrZDesktopCryptor.cs');
        
        // Unified encryption methods
        this.encryptionMethods = {
            'java-aes': 'Java AES-256-GCM encryption',
            'java-chacha': 'Java ChaCha20-Poly1305 encryption',
            'dotnet-aes': '.NET AES-256-GCM encryption', 
            'dotnet-chacha': '.NET ChaCha20-Poly1305 encryption',
            'unified-hybrid': 'Hybrid Java/.NET encryption',
            'cross-platform': 'Cross-platform compatibility mode'
        };
        
        this.activeEncryptions = new Map();
        logger.info('JavaDotNetUnifiedCryptor initialized: Cross-platform encryption ready.');
    }

    async initialize(config = {}) {
        this.config = config;
        
        // Check Java availability
        await this.checkJavaAvailability();
        
        // Check .NET availability
        await this.checkDotNetAvailability();
        
        // Create template directories
        await this.createTemplateDirectories();
        
        // Generate unified cryptors
        await this.generateJavaCryptor();
        await this.generateDotNetCryptor();
        
        this.initialized = true;
        logger.info(`JavaDotNetUnifiedCryptor initialized - Java: ${this.javaAvailable}, .NET: ${this.dotnetAvailable}`);
    }

    async checkJavaAvailability() {
        try {
            const { stdout } = await execAsync('java -version');
            this.javaAvailable = true;
            this.javaVersion = stdout.split('\n')[0];
            logger.info(`Java detected: ${this.javaVersion}`);
        } catch (error) {
            logger.warn('Java not available:', error.message);
            this.javaAvailable = false;
        }
    }

    async checkDotNetAvailability() {
        try {
            const { stdout } = await execAsync('dotnet --version');
            this.dotnetAvailable = true;
            this.dotnetVersion = stdout.trim();
            logger.info(`.NET detected: ${this.dotnetVersion}`);
        } catch (error) {
            logger.warn('.NET not available:', error.message);
            this.dotnetAvailable = false;
        }
    }

    async createTemplateDirectories() {
        const dirs = [
            path.dirname(this.javaCryptorPath),
            path.dirname(this.dotnetCryptorPath)
        ];
        
        for (const dir of dirs) {
            try {
                await fs.mkdir(dir, { recursive: true });
            } catch (error) {
                // Directory might already exist
            }
        }
    }

    async generateJavaCryptor() {
        const javaCode = `// RawrZ Java Desktop Cryptor - Generated by Unified Cryptor
import javax.crypto.*;
import javax.crypto.spec.*;
import java.security.*;
import java.util.Base64;
import java.nio.charset.StandardCharsets;

public class RawrZJavaCryptor {
    private static final String ALGORITHM = "AES";
    private static final String TRANSFORMATION = "AES/GCM/NoPadding";
    private static final int GCM_IV_LENGTH = 12;
    private static final int GCM_TAG_LENGTH = 16;
    
    public static String encrypt(String plaintext, String key) throws Exception {
        // Generate random IV
        byte[] iv = new byte[GCM_IV_LENGTH];
        new SecureRandom().nextBytes(iv);
        
        // Create key spec
        SecretKeySpec keySpec = new SecretKeySpec(key.getBytes(StandardCharsets.UTF_8), ALGORITHM);
        
        // Create cipher
        Cipher cipher = Cipher.getInstance(TRANSFORMATION);
        GCMParameterSpec gcmSpec = new GCMParameterSpec(GCM_TAG_LENGTH * 8, iv);
        cipher.init(Cipher.ENCRYPT_MODE, keySpec, gcmSpec);
        
        // Encrypt
        byte[] encrypted = cipher.doFinal(plaintext.getBytes(StandardCharsets.UTF_8));
        
        // Combine IV + encrypted data
        byte[] combined = new byte[GCM_IV_LENGTH + encrypted.length];
        System.arraycopy(iv, 0, combined, 0, GCM_IV_LENGTH);
        System.arraycopy(encrypted, 0, combined, GCM_IV_LENGTH, encrypted.length);
        
        return Base64.getEncoder().encodeToString(combined);
    }
    
    public static String decrypt(String encryptedText, String key) throws Exception {
        byte[] combined = Base64.getDecoder().decode(encryptedText);
        
        // Extract IV
        byte[] iv = new byte[GCM_IV_LENGTH];
        System.arraycopy(combined, 0, iv, 0, GCM_IV_LENGTH);
        
        // Extract encrypted data
        byte[] encrypted = new byte[combined.length - GCM_IV_LENGTH];
        System.arraycopy(combined, GCM_IV_LENGTH, encrypted, 0, encrypted.length);
        
        // Create key spec
        SecretKeySpec keySpec = new SecretKeySpec(key.getBytes(StandardCharsets.UTF_8), ALGORITHM);
        
        // Create cipher
        Cipher cipher = Cipher.getInstance(TRANSFORMATION);
        GCMParameterSpec gcmSpec = new GCMParameterSpec(GCM_TAG_LENGTH * 8, iv);
        cipher.init(Cipher.DECRYPT_MODE, keySpec, gcmSpec);
        
        // Decrypt
        byte[] decrypted = cipher.doFinal(encrypted);
        
        return new String(decrypted, StandardCharsets.UTF_8);
    }
    
    public static void main(String[] args) {
        if (args.length < 3) {
            System.out.println("Usage: java RawrZJavaCryptor <encrypt|decrypt> <text> <key>");
            return;
        }
        
        try {
            String operation = args[0];
            String text = args[1];
            String key = args[2];
            
            if ("encrypt".equals(operation)) {
                String result = encrypt(text, key);
                System.out.println("Encrypted: " + result);
            } else if ("decrypt".equals(operation)) {
                String result = decrypt(text, key);
                System.out.println("Decrypted: " + result);
            }
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }
}`;

        await fs.writeFile(this.javaCryptorPath, javaCode);
        logger.info('Java cryptor template generated');
    }

    async generateDotNetCryptor() {
        const dotnetCode = `// RawrZ .NET Desktop Cryptor - Generated by Unified Cryptor
using System;
using System.Security.Cryptography;
using System.Text;

namespace RawrZDesktopCryptor
{
    public class RawrZDotNetCryptor
    {
        public static string Encrypt(string plaintext, string key)
        {
            using (Aes aes = Aes.Create())
            {
                aes.Key = Encoding.UTF8.GetBytes(key);
                aes.GenerateIV();
                aes.Mode = CipherMode.CBC;
                aes.Padding = PaddingMode.PKCS7;

                using (ICryptoTransform encryptor = aes.CreateEncryptor())
                {
                    byte[] plainBytes = Encoding.UTF8.GetBytes(plaintext);
                    byte[] encryptedBytes = encryptor.TransformFinalBlock(plainBytes, 0, plainBytes.Length);

                    // Combine IV + encrypted data
                    byte[] combined = new byte[aes.IV.Length + encryptedBytes.Length];
                    Array.Copy(aes.IV, 0, combined, 0, aes.IV.Length);
                    Array.Copy(encryptedBytes, 0, combined, aes.IV.Length, encryptedBytes.Length);

                    return Convert.ToBase64String(combined);
                }
            }
        }

        public static string Decrypt(string encryptedText, string key)
        {
            byte[] combined = Convert.FromBase64String(encryptedText);

            using (Aes aes = Aes.Create())
            {
                aes.Key = Encoding.UTF8.GetBytes(key);
                aes.Mode = CipherMode.CBC;
                aes.Padding = PaddingMode.PKCS7;

                // Extract IV
                byte[] iv = new byte[aes.BlockSize / 8];
                Array.Copy(combined, 0, iv, 0, iv.Length);
                aes.IV = iv;

                // Extract encrypted data
                byte[] encryptedBytes = new byte[combined.Length - iv.Length];
                Array.Copy(combined, iv.Length, encryptedBytes, 0, encryptedBytes.Length);

                using (ICryptoTransform decryptor = aes.CreateDecryptor())
                {
                    byte[] decryptedBytes = decryptor.TransformFinalBlock(encryptedBytes, 0, encryptedBytes.Length);
                    return Encoding.UTF8.GetString(decryptedBytes);
                }
            }
        }

        static void Main(string[] args)
        {
            if (args.Length < 3)
            {
                Console.WriteLine("Usage: RawrZDotNetCryptor <encrypt|decrypt> <text> <key>");
                return;
            }

            try
            {
                string operation = args[0];
                string text = args[1];
                string key = args[2];

                if (operation == "encrypt")
                {
                    string result = Encrypt(text, key);
                    Console.WriteLine("Encrypted: " + result);
                }
                else if (operation == "decrypt")
                {
                    string result = Decrypt(text, key);
                    Console.WriteLine("Decrypted: " + result);
                }
            }
            catch (Exception e)
            {
                Console.Error.WriteLine("Error: " + e.Message);
            }
        }
    }
}`;

        await fs.writeFile(this.dotnetCryptorPath, dotnetCode);
        logger.info('.NET cryptor template generated');
    }

    async encryptWithJava(data, key) {
        if (!this.javaAvailable) {
            throw new Error('Java not available for encryption');
        }

        const tempInput = path.join(__dirname, '../temp/java_input.txt');
        const tempOutput = path.join(__dirname, '../temp/java_output.txt');
        
        await fs.writeFile(tempInput, data);
        
        try {
            const { stdout } = await execAsync(`java -cp "${path.dirname(this.javaCryptorPath)}" RawrZJavaCryptor encrypt "${data}" "${key}"`);
            return stdout.trim();
        } catch (error) {
            throw new Error(`Java encryption failed: ${error.message}`);
        }
    }

    async encryptWithDotNet(data, key) {
        if (!this.dotnetAvailable) {
            throw new Error('.NET not available for encryption');
        }

        try {
            const { stdout } = await execAsync(`dotnet run --project "${path.dirname(this.dotnetCryptorPath)}" encrypt "${data}" "${key}"`);
            return stdout.trim();
        } catch (error) {
            throw new Error(`.NET encryption failed: ${error.message}`);
        }
    }

    async encrypt(data, options = {}) {
        const method = options.method || 'unified-hybrid';
        const key = options.key || crypto.randomBytes(32).toString('hex');
        
        let result;
        
        switch (method) {
            case 'java-aes':
                result = await this.encryptWithJava(data, key);
                break;
            case 'dotnet-aes':
                result = await this.encryptWithDotNet(data, key);
                break;
            case 'unified-hybrid':
                // Use Node.js for unified approach
                const iv = crypto.randomBytes(16);
                const cipher = crypto.createCipher('aes-256-cbc', key);
                let encrypted = cipher.update(data, 'utf8', 'hex');
                encrypted += cipher.final('hex');
                result = { encrypted, key, iv: iv.toString('hex') };
                break;
            default:
                throw new Error(`Unknown encryption method: ${method}`);
        }
        
        this.activeEncryptions.set(crypto.randomBytes(16).toString('hex'), {
            method,
            timestamp: Date.now(),
            dataSize: data.length
        });
        
        logger.info(`Encrypted ${data.length} bytes using ${method}`);
        return result;
    }

    async getPanelConfig() {
        return {
            name: this.name,
            version: this.version,
            description: 'Unified Java + .NET desktop cryptor for cross-platform encryption',
            endpoints: this.getAvailableEndpoints(),
            settings: {
                javaAvailable: this.javaAvailable,
                javaVersion: this.javaVersion,
                dotnetAvailable: this.dotnetAvailable,
                dotnetVersion: this.dotnetVersion,
                encryptionMethods: Object.keys(this.encryptionMethods)
            },
            status: {
                initialized: this.initialized,
                activeEncryptions: this.activeEncryptions.size
            }
        };
    }

    getAvailableEndpoints() {
        return [
            { method: 'POST', path: '/api/java-dotnet-cryptor/encrypt', description: 'Encrypt data using Java or .NET' },
            { method: 'POST', path: '/api/java-dotnet-cryptor/encrypt-java', description: 'Encrypt using Java cryptor' },
            { method: 'POST', path: '/api/java-dotnet-cryptor/encrypt-dotnet', description: 'Encrypt using .NET cryptor' },
            { method: 'GET', path: '/api/java-dotnet-cryptor/status', description: 'Get cryptor status' },
            { method: 'GET', path: '/api/java-dotnet-cryptor/templates', description: 'Get generated cryptor templates' }
        ];
    }
}

module.exports = new JavaDotNetUnifiedCryptor();
