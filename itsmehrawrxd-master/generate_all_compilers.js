// Generate master compiler that includes all languages
const masterCompiler = `; Master Universal Compiler - All Languages From Scratch
; NASM x86-64 Assembly Implementation
; Generated by the same system that built the 138,721-line EON IDE

section .data
    master_compiler_name db "Master Universal Compiler", 0
    master_compiler_version db "1.0.0", 0
    supported_languages_count dd ${languages.length}
    
    ; Include all language compilers
    ${languages.map(lang => `%include "${lang.toLowerCase().replace(/[^a-z0-9]/g, '_')}_compiler_from_scratch.asm"`).join('\n    ')}
    
section .text
    global master_compiler_init
    global master_compiler_compile
    global master_compiler_get_supported_languages
    global master_compiler_cleanup

master_compiler_init:
    push rbp
    mov rbp, rsp
    
    ; Initialize all language compilers
    ${languages.map(lang => {
        const langName = lang.toLowerCase().replace(/[^a-z0-9]/g, '_');
        return `call ${langName}_compiler_init`;
    }).join('\n    ')}
    
    pop rbp
    ret

master_compiler_compile:
    push rbp
    mov rbp, rsp
    
    ; rdi: language ID
    ; rsi: source code buffer
    ; rdx: source code size
    ; rcx: output buffer
    ; r8: output buffer size
    
    ; Route to appropriate language compiler
    ${languages.map((lang, index) => {
        const langName = lang.toLowerCase().replace(/[^a-z0-9]/g, '_');
        return `cmp rdi, ${index + 1}\n    je .compile_${langName}\n    `;
    }).join('')}
    jmp .unsupported_language
    
    ${languages.map((lang, index) => {
        const langName = lang.toLowerCase().replace(/[^a-z0-9]/g, '_');
        return `.compile_${langName}:\n    call ${langName}_compiler_compile\n    jmp .done\n    `;
    }).join('')}
    
.unsupported_language:
    mov rax, -1
    jmp .done
    
.done:
    pop rbp
    ret

master_compiler_get_supported_languages:
    push rbp
    mov rbp, rsp
    
    ; Return number of supported languages
    mov rax, [supported_languages_count]
    
    pop rbp
    ret

master_compiler_cleanup:
    push rbp
    mov rbp, rsp
    
    ; Cleanup all language compilers
    ${languages.map(lang => {
        const langName = lang.toLowerCase().replace(/[^a-z0-9]/g, '_');
        return `call ${langName}_compiler_cleanup`;
    }).join('\n    ')}
    
    pop rbp
    ret`;

