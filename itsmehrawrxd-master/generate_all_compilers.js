function generateCompilerFunctions(language) {
    const langName = language.toLowerCase().replace(/[^a-z0-9]/g, '_');
    
    return `
${langName}_compiler_init:
    push rbp
    mov rbp, rsp
    
    ; Initialize ${language} compiler
    mov dword [compiler_state], 0
    mov dword [error_count], 0
    mov dword [warning_count], 0
    
    pop rbp
    ret

${langName}_compiler_compile:
    push rbp
    mov rbp, rsp
    
    ; rdi: source code buffer
    ; rsi: source code size
    ; rdx: output buffer
    ; rcx: output buffer size
    
    ; Phase 1: Lexical Analysis
    call ${langName}_lexer
    
    ; Phase 2: Syntax Analysis  
    call ${langName}_parser
    
    ; Phase 3: Semantic Analysis
    call ${langName}_semantic_analyzer
    
    ; Phase 4: Code Generation
    call ${langName}_code_generator
    
    ; Phase 5: Optimization
    call ${langName}_optimizer
    
    pop rbp
    ret

${langName}_compiler_cleanup:
    push rbp
    mov rbp, rsp
    
    ; Cleanup ${language} compiler resources
    mov dword [compiler_state], 0
    
    pop rbp
    ret

; ${language} specific functions
${langName}_lexer:
    push rbp
    mov rbp, rsp
    ; ${language} lexical analysis
    pop rbp
    ret

${langName}_parser:
    push rbp
    mov rbp, rsp
    ; ${language} syntax analysis
    pop rbp
    ret

${langName}_semantic_analyzer:
    push rbp
    mov rbp, rsp
    ; ${language} semantic analysis
    pop rbp
    ret

${langName}_code_generator:
    push rbp
    mov rbp, rsp
    ; ${language} code generation
    pop rbp
    ret

${langName}_optimizer:
    push rbp
    mov rbp, rsp
    ; ${language} optimization
    pop rbp
    ret`;
}

