    def compile(self, source_file: str, output_file: str = None) -> bool:
        """Compile source file to ANY extension"""
        try:
            if not output_file:
                output_file = self._get_output_filename(source_file)
            
            # Detect target extension
            target_ext = os.path.splitext(output_file)[1].lower()
            
            print(f"🔨 Compiling {source_file} to {output_file} ({target_ext})...")
            
            # Read source file
            with open(source_file, 'r', encoding='utf-8') as f:
                source_code = f.read()
            
            # Get handler for extension
            handler = self.extension_handlers.get(target_ext, self._compile_to_unknown)
            
            # Compile to target extension
            success = handler(source_code, output_file)
            
            if success:
                print(f"✅ Successfully compiled to {target_ext}: {output_file}")
                return True
            else:
                print(f"❌ Failed to compile to {target_ext}")
                return False
                
        except Exception as e:
            print(f"❌ Compilation failed: {str(e)}")
            return False
    
