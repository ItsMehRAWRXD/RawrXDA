    def _compile_to_png(self, source_code: str, output_file: str) -> bool:
        """Compile to PNG image"""
        try:
            from PIL import Image, ImageDraw, ImageFont
            # Create image with code
            img = Image.new('RGB', (800, 600), color='white')
            draw = ImageDraw.Draw(img)
            try:
                font = ImageFont.truetype("arial.ttf", 12)
            except:
                font = ImageFont.load_default()
            
            y = 10
            for line in source_code.split('\n')[:40]:  # Limit to 40 lines
                draw.text((10, y), line, fill='black', font=font)
                y += 15
            
            img.save(output_file)
            return True
        except ImportError:
            print("PIL not available, creating text file instead")
            return self._compile_to_text(source_code, output_file)
    
