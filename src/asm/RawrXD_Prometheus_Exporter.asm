; =============================================================================
; RawrXD_Prometheus_Exporter.asm — Pure x64 MASM
; Formats UTC binary counters into Prometheus Text Format (v0.0.4)
; =============================================================================
;
; Architecture:
;   - Zero-allocation formatting: writes directly into a caller-provided buffer
;   - Custom high-speed U64-to-ASCII (division-based, in-place reverse)
;   - No sprintf, no CRT, no heap churn
;   - Directly consumable by Prometheus /metrics scrape endpoint
;
; Exports:
;   ExportPrometheus          — Full metrics payload into buffer
;   ExportPrometheusMetric    — Single metric line into buffer
;   Internal_U64ToAscii       — 64-bit integer to ASCII conversion
;   Internal_AppendString     — Fast memcpy-style string append
;
; Pattern: Returns RAX = total bytes written
; Rule:    NO CRT, NO Qt, NO exceptions, NO heap
; =============================================================================

include RawrXD_Common.inc

; Import counter symbols from RawrXD_Telemetry_Kernel.asm
EXTERNDEF g_Counter_Inference:QWORD
EXTERNDEF g_Counter_ScsiFails:QWORD
EXTERNDEF g_Counter_AgentLoop:QWORD
EXTERNDEF g_Counter_BytePatches:QWORD
EXTERNDEF g_Counter_MemPatches:QWORD
EXTERNDEF g_Counter_ServerPatches:QWORD
EXTERNDEF g_Counter_FlushOps:QWORD
EXTERNDEF g_Counter_Errors:QWORD

