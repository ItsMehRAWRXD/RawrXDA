; =============================================================================
; rawr_globals.asm — Centralized Global State for RawrXD (x64 MASM64)
; =============================================================================
;
; Single translation unit that OWNS all cross-module shared state.
; Every other .asm file must reference these symbols via EXTERN / EXTERNDEF
; (provided by rawr_globals.inc).
;
; Categories:
;   1. CPU Feature Flags      — CPUID results cached at init
;   2. Dispatch Pointers      — Function pointers for runtime dispatch
;   3. Telemetry Counters     — Cache-line padded atomic counters
;   4. Shell Integration      — Cache-line padded shell metrics
;   5. Agentic Shell Bridge   — Cache-line padded AI bridge metrics
;   6. Enterprise License     — Feature gate flags
;   7. Flash Attention        — AVX-512 readiness + perf counters
;   8. Inference Stats        — GEMM/GEMV accumulated metrics
;
; Build: ml64.exe /c /Zi /Zd /I"src/asm" rawr_globals.asm
; Rule:  NO SOURCE FILE IS TO BE SIMPLIFIED
; =============================================================================

option casemap:none

; =============================================================================
;  1. CPU Feature Flags + Inference Dispatch  (64-byte aligned)
; =============================================================================
_DATA64 SEGMENT ALIGN(64) 'DATA'

; ─── CPUID Feature Flags (set by InferenceCore_Init) ─────────────────────────
PUBLIC g_HasAVX2
PUBLIC g_HasFMA3
PUBLIC g_HasAVX512F

g_HasAVX2               DD 0
g_HasFMA3               DD 0
g_HasAVX512F            DD 0
                        DD 0            ; pad to 16 bytes

; ─── Inference Performance Counters ──────────────────────────────────────────
PUBLIC g_GemmCalls
PUBLIC g_GemvCalls
PUBLIC g_GemmFlops

g_GemmCalls             DQ 0
g_GemvCalls             DQ 0
g_GemmFlops             DQ 0            ; Accumulated FLOPs

; ─── Dispatch Function Pointers (set by Init to best available path) ─────────
PUBLIC g_GemmDispatch
PUBLIC g_GemvDispatch

g_GemmDispatch          DQ 0            ; -> sgemm_avx2 or sgemm_avx512
g_GemvDispatch          DQ 0            ; -> sgemv_avx2 or sgemv_avx512

; ─── Flash Attention AVX-512 ─────────────────────────────────────────────────
; Broadcast-ready constants (64-byte aligned for ZMM loads)
PUBLIC g_NegInf
PUBLIC g_AVX512Ready
PUBLIC g_FlashAttnCalls
PUBLIC g_FlashAttnTiles

ALIGN 64
g_NegInf                DD 16 DUP(0FF800000h)  ; -inf x16 (one ZMM)

g_AVX512Ready           DD 0

ALIGN 8
g_FlashAttnCalls        DQ 0
g_FlashAttnTiles        DQ 0

_DATA64 ENDS

; =============================================================================
;  2. Enterprise License Flags
; =============================================================================
.data
ALIGN 16

PUBLIC g_800B_Unlocked
PUBLIC g_EnterpriseFeatures

g_800B_Unlocked         DD 0
g_EnterpriseFeatures    DQ 0

; =============================================================================
;  3. Telemetry Counters  (cache-line padded to prevent false sharing)
; =============================================================================
; Each entry is manually padded to 64 bytes for cache-line isolation.
; ml64 maximum segment alignment is 16; data self-pads to 64.

TELEMETRY_CL SEGMENT ALIGN(16) 'DATA'

    PUBLIC g_MetricTableStart
    g_MetricTableStart      DQ 0DEADBEEFCAFEBABEh   ; Sentinel / magic
                            DB 56 DUP(0)             ; Pad to 64 bytes

    PUBLIC g_Counter_Inference
    g_Counter_Inference     DQ 0    ; Total tokens generated by ASM kernels
                            DB 56 DUP(0)

    PUBLIC g_Counter_ScsiFails
    g_Counter_ScsiFails     DQ 0    ; Hardware SCSI timeout count
                            DB 56 DUP(0)

    PUBLIC g_Counter_AgentLoop
