                            DB 56 DUP(0)

    PUBLIC g_MetricTableEnd
    g_MetricTableEnd        DQ 0BADCAFEDEADFACEh   ; Sentinel / end marker
                            DB 56 DUP(0)

TELEMETRY_CL ENDS

