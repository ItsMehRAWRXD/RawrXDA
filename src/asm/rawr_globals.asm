                            DB 56 DUP(0)

    PUBLIC g_Counter_BytePatches
    g_Counter_BytePatches   DQ 0    ; Byte-level hotpatches applied
                            DB 56 DUP(0)

    PUBLIC g_Counter_MemPatches
    g_Counter_MemPatches    DQ 0    ; Memory hotpatches applied
                            DB 56 DUP(0)

    PUBLIC g_Counter_ServerPatches
    g_Counter_ServerPatches DQ 0    ; Server-layer hotpatches applied
                            DB 56 DUP(0)

    PUBLIC g_Counter_FlushOps
