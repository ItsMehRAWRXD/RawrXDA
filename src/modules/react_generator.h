    static bool RegenerateAgentModePanel(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateEngineManager(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateMemoryViewer(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateToolOutputPanel(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateHotpatchControls(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateREToolsPanel(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool RegenerateMainIDEApp(const std::filesystem::path& dir, const ReactServerConfig& config);

