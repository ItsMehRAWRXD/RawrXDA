    static bool GenerateGitignore(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateDockerfile(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateTestFiles(const std::filesystem::path& dir, const ReactServerConfig& config);
    
    // IDE-specific component generation
    static bool GenerateIDEComponents(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateMonacoEditor(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateAgentModePanel(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateEngineManager(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateMemoryViewer(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateToolOutputPanel(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateHotpatchControls(const std::filesystem::path& dir, const ReactServerConfig& config);
    static bool GenerateREToolsPanel(const std::filesystem::path& dir, const ReactServerConfig& config);
